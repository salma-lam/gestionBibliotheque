    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        if (txt_email.getText().equals("") || txt_login.getText().equals("") ) {
            JOptionPane.showMessageDialog(this, "SVP entrer les informations compl√®tes");
        } else {

        rs = db.querySelectAll("administrateur", "Email='" + txt_email.getText() + "' and Mot_passe='" + txt_login.getText() + "'");
        try {
             while (rs.next()) {
                email = rs.getString("Email");
                mot_passe = rs.getString("Mot_passe");
                hak = rs.getString("type");
                
                
            } 
        } catch (SQLException ex) {
            Logger.getLogger(LOGIN.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (email == null && mot_passe == null) {
            JOptionPane.showMessageDialog(this,"le nom utilisateur ou le mots de pass est incorrect");
          
            P.setText("Le login ou le mot de passe est incorrect");
            
            }else{
           if (hak.equals("Responsable")) {
                Principal h = new Principal();
                h.setVisible(true);
                this.dispose();
            } else {
                Principal_A k = null;
               try {
                   k = new Principal_A();
               } catch (SQLException ex) {
                   Logger.getLogger(LOGIN.class.getName()).log(Level.SEVERE, null, ex);
               }
                k.setVisible(true);
                this.dispose();
                
            }
    }
   }       
    }//GEN-LAST:event_jButton1ActionPerformed

