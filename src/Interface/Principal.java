    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (txt_nom.getText().equals("") || txt_prenom.getText().equals("") || txt_email.getText().equals("")
            || txt_mot_passe.getText().equals("")|| compty.getSelectedItem().equals("Typpppe") ) {
            JOptionPane.showMessageDialog(this, "SVP entrer les informations complètes");
        }else{
            String id = String.valueOf(table_admin.getValueAt(table_admin.getSelectedRow(), 0));
            if (JOptionPane.showConfirmDialog(this, "Etes-vous sûr que vous voulez supprimer ?", "attention!!!", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                db.queryDelete("administrateur", "idAdministrateur=" + id);
            } else {
                return;
            }
            table1();
            pp5.setText("Supprimer avec succès");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

