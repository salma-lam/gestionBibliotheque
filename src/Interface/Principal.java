    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (txt_rech.getText().equals("")  ) {
            JOptionPane.showMessageDialog(this, "SVP entrer vos donneés");
        } else {
            if (com_re.getSelectedItem().equals("idAdministrateur")) {
                rs = db.querySelectAll("administrateur", "idAdministrateur LIKE '%" + txt_rech.getText() + "%' ");
                table_admin.setModel(new Gestion_biblio(rs));
            } else if (com_re.getSelectedItem().equals("Nom")) {
                rs = db.querySelectAll("administrateur", "Nom LIKE '%" + txt_rech.getText() + "%' ");
                table_admin.setModel(new Gestion_biblio(rs));
            } else if (com_re.getSelectedItem().equals("Prénom")) {
                rs = db.querySelectAll("administrateur", "Prénom LIKE '%" + txt_rech.getText() + "%' ");
                table_admin.setModel(new Gestion_biblio(rs));
            } else if (com_re.getSelectedItem().equals("Email")) {
                rs = db.querySelectAll("administrateur", "Email LIKE '%" + txt_rech.getText() + "%' ");
                table_admin.setModel(new Gestion_biblio(rs));

            } else if (com_re.getSelectedItem().equals("Type")) {
                rs = db.querySelectAll("administrateur", "type LIKE '%" + txt_rech.getText() + "%' ");
                table_admin.setModel(new Gestion_biblio(rs));

            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

